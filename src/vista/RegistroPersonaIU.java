    public void limpiarTablaPersona(){
        for (int i = modeloTablaPersona.getRowCount()-1; i >=0 ; i--) {
            modeloTablaPersona.removeRow(i);
        }
    }
    /*
    * Actualizar el modelo tabla
    */
    public void actualizarTablaPersona(boolean refrescar){
        if(refrescar){
            gestorPersona.refrescar();
        }
        // Limpiar la TablaPersona 
        limpiarTablaPersona();
        // Recuperar los datos de Gestion Persona
        for (int i = 0; i < gestorPersona.longitud(); i++) {
            String[] fila = new String[4]; // DNI, Nombres, Sexo, LugarNacimiento           
            int dni = gestorPersona.iesimo(i).getDni();
            // Convertir un entero a texto
            fila[0] = String.valueOf(dni);
            fila[1] = gestorPersona.iesimo(i).getNombres();
            fila[2] = String.valueOf(gestorPersona.iesimo(i).getSexo());
            fila[3] = gestorPersona.iesimo(i).getLugarNacimiento().getNombre();
            // Agregar un registro al modelo Tabla
            modeloTablaPersona.addRow(fila);
        }
        // Agragar los datos a la TablaPersona
    }
    

