    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        // Recuperar los datos de la persona del Formulario
        int dni = Integer.valueOf(txtDNI.getText());
        String nombres = txtNombres.getText();
        //Recuperar el Sexo
        char sexo = '\000';
        if (rbSexoFemenino.isSelected()){
            sexo = 'F';
        }else{
            sexo = 'M';
        }
        // Recuperar el Lugar de nacimiento
        Lugar lugarNacimiento = (Lugar) cboLugarNacimiento.getSelectedItem();
        Persona objPersona = new Persona(dni,nombres,sexo,lugarNacimiento);
        // Registrar a la persona en el arreglo de Personas
        gestorPersona.agregar(objPersona);
        // Actualizar la tabla Persona en la Interfaz
        actualizarTablaPersona(true);
        JOptionPane.showMessageDialog(rootPane, "Se agrego con Exito..");        
        // Limpiar el Formulario
        txtDNI.setText("");
        txtNombres.setText("");
        txtDNI.requestFocus(); // Ubicar el cursos en la Caja de Texto
    }//GEN-LAST:event_btnAgregarActionPerformed

